    private void tbmonanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbmonanMouseClicked
        try{
        //Lay chi so dong dang chon
                int row =this.tbmonan.getSelectedRow();
                String ml=  (this.tbmonan.getModel().getValueAt(row,0)).toString();
                int a = Integer.parseInt(ml);
                ResultSet rs= lsp.ShowMon(a);//Goi ham lay du lieu theo ma loai
            if(rs.next())//Neu co du lieu
            {
                this.tfmon.setText(rs.getString("TENMON"));
                this.tfgia.setText(rs.getString("Gia"));
            }
        }
        catch (SQLException e) {
        }
    }//GEN-LAST:event_tbmonanMouseClicked

