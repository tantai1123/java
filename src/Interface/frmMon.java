    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        String ml=tfmon.getText();
        int row =this.tbmonan.getSelectedRow();
        String stt=  (this.tbmonan.getModel().getValueAt(row,0)).toString();
        int a = Integer.parseInt(stt);
        try {
            if(ml.length()==0)
            JOptionPane.showMessageDialog(null,"Chọn để xóa",
                "Thông báo",1);
            else
            {
                if( JOptionPane.showConfirmDialog(null, "Bạn muốn xóa món ăn " + ml + " hay không ?","Thông báo",2) == 0)
                {
                    lsp.DeleteData(a);//goi ham xoa du lieu theo ma loai
                    ClearData();//Xoa du lieu trong tableModel
                    ShowData();//Do du lieu vao table Model
                    setNull();//Xoa trang Textfield
                }
            }
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Xóa thất bại","Thong bao",1);
        }
    }//GEN-LAST:event_btnxoaActionPerformed

