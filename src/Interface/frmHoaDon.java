    private void btnluuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnluuMouseClicked
        int row = this.tbhoadon.getSelectedRow();
        String stt = (this.tbhoadon.getModel().getValueAt(row, 0)).toString();
        int a = Integer.parseInt(stt);

        String ban = tfban.getText().trim();
        int b = Integer.parseInt(ban);
        String ca = tfca.getText().trim();
        String nlhd = tfnguoilaphd.getText().trim();
        int c = Integer.parseInt(nlhd);
        String tkh = tftenkhachhang.getText().trim();
        Date ngaylap = (Date) dtngaylap.getDate();
        Date giovao = (Date) dtgiovao.getDate();
        Date giothanhtoan = (Date) dtgiothanhtoan.getDate();
        int ad = 1;

        if (ban.length() == 0 || tkh.length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ", "Thông báo", 1);
        } else
        try {
            if (cothem == true) //Luu cho tthem moi
            {
                lsp.InsertData(b, ngaylap, giovao, giothanhtoan, ca, tkh, c);
            } else //Luu cho sua
            {
                lsp.EditData(a, b, ngaylap, giovao, giothanhtoan, ca, tkh, c);
            }
            ClearData(); //goi ham xoa du lieu tron tableModel
            ShowData(); //Do lai du lieu vao Table Model
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Thông báo", 1);
        }
        setKhoa(false);
        setButton(true);
    }//GEN-LAST:event_btnluuMouseClicked

