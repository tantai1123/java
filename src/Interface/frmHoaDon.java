    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        String ml = tfban.getText();
        int row = this.tbhoadon.getSelectedRow();
        String stt = (this.tbhoadon.getModel().getValueAt(row, 0)).toString();
        int a = Integer.parseInt(stt);
        try {
            if (ml.length() == 0) {
                JOptionPane.showMessageDialog(null, "Chọn để xóa",
                        "Thông báo", 1);
            } else {
                if (JOptionPane.showConfirmDialog(null, "Bạn muốn xóa bàn số " + ml + " hay không ?", "Thông báo", 2) == 0) {
                    lsp.DeleteData(a);//goi ham xoa du lieu theo ma loai
                    ClearData();//Xoa du lieu trong tableModel
                    ShowData();//Do du lieu vao table Model
                    setNull();//Xoa trang Textfield
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Xóa thất bại", "Thông báo", 1);
        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void tbhoadonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbhoadonMouseClicked
        try {
            //Lay chi so dong dang chon
            int row = this.tbhoadon.getSelectedRow();
            String ml = (this.tbhoadon.getModel().getValueAt(row, 0)).toString();
            int a = Integer.parseInt(ml);
            ResultSet rs = lsp.ShowHoaDon(a);//Goi ham lay du lieu theo ma loai
            if (rs.next())//Neu co du lieu
            {
                this.tfban.setText(rs.getString("IDBAN"));
                this.tfca.setText(rs.getString("CA"));
                this.tfnguoilaphd.setText(rs.getString("IDNGUOIDUNG"));
                this.tftenkhachhang.setText(rs.getString("TENKHACHHANG"));
                this.dtngaylap.setDate(rs.getDate("NGAYLAP"));
                this.dtgiovao.setDate(rs.getDate("GIOVAO"));
                this.dtgiothanhtoan.setDate(rs.getDate("GIOTHANHTOAN"));
            }
        } catch (SQLException e) {
        }
    }//GEN-LAST:event_tbhoadonMouseClicked

